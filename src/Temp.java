public class Temp {
   public static void main(String[] args) {
        int temp = 21;
        if (temp > 25) {
            System.out.println("Status: The air conditioner is on.");
        } else if (temp < 22) {
            System.out.println("Status: The air conditioner is off.");
        } else {
            System.out.println("Status: The air conditioner is inactive.");
        }
    }

}
